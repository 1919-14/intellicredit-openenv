"""
IntelliCredit v2 — Agent Execution Loop
========================================
The central orchestrator between the LLM and the environment.

Responsibilities:
  1. Parse LLM free-form output (via action_parser)
  2. Execute tool calls against live env (via tool_executor)
  3. Enforce 4-call limit + redundancy detection
  4. Submit final decisions and advance the environment step
  5. Compute tool-efficiency reward bonuses/penalties
  6. Emit structured per-step logs for GRPO training

Key functions:
  process_llm_step(llm_output, env, step_state) → StepResult
  build_system_prompt(obs)                       → str
  build_tool_result_message(tool_result)         → str
  create_step_state()                            → StepState dict
"""

